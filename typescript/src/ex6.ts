class Queue<T> {
    list: T[] = [];
    get length() {
        return this.list.length;
    }
    enqueue(item: T) {
        this.list.push(item);
    }
    dequeue() {
        return this.list.shift();
    }
}

const queue = new Queue<any>();

queue.enqueue('1');
queue.enqueue(2);
queue.enqueue(3);
queue.enqueue('4');
queue.enqueue(5);
console.log(queue.dequeue());
